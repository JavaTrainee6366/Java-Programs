package Encapsulations;

public class MyBean {
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getXyz() {
		return xyz;
	}
	public void setXyz(float xyz) {
		this.xyz = xyz;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	private String name;
	private float xyz;
	private int id;
	
	
	

}
